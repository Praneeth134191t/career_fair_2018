            <h2 class="major">INTECS Outreach</h2>
            <span class="image main"><img src="http://i.imgur.com/BHdSH6i.jpg" alt="Group Photo at INTECS Outreach" /></span>
            <p class="center-item">INTECS Outreach (previously known as INTECS college visit), one of the highlights of the INTECS calander is one where the undergraduates from the faculty give back to the society by visiting schools across the country in hope of "Inspiring The Next Generation".</p>
            <span style="width: 50%; float: left" class="image main"><img src="http://i.imgur.com/J4zdNIF.jpg" alt="School Children at INTECS Outreach" /></span>
            <span style="width: 50%; float: left" class="image main"><img src="http://i.imgur.com/l1hHQj9.jpg" alt="Motivational Session at INTECS Outreach" /></span>
            <p class="center-item">The main aim of the school visits is to help the Advanced Level ICT students as well as Ordinary Level ICT students to prepare for their G.C.E. examinations. This is done by the faculty undergraduates with the conducting Lab Sessions, Revision Sessions and Theory Sessions depending on the request from the school children. With the number of volunteers from the faculty, individual attention is given to the ICT students to help them overcome any academic hurdle they face.</p>
            <p class="center-item">In addition to this, a Motivational Session targeted at all the Advanced Level students in general, increases the students focus towards passing their examinations and entering a University. The success of the entire event is measured by the numerous feedback that is received from the students.</p>
            <span class="image main"><img src="http://i.imgur.com/MV8Ykhh.jpg" alt="Feedback at INTECS Outreach" /></span>
            <p class="center-item">Want to be a part of INTECS Outreach and do your part to the Next Generation? Contact us through our facebook page</p>
