            <span class="image main"><img src="http://i.imgur.com/LKex00z.jpg" alt="FIT CodeRush Participants" /></span>
            <p class="center-item">FIT CodeRush having being inagurated in 2016 is an in-house coding competition for the IT faculty undergraduates. Using the famous HackerRank platform an all night 12 hour coding competition with the participation of over 50 teams brings out the best of what the IT faculty has to offer. With a practice round and guidance from seniors inside and outside the faculty, INTECS takes steps to improve the quality of the output of the faculty by organizing such events. And for all those who are wondering, FIT CodeRush 2017 is right around the corner.</p>
